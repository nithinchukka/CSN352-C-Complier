int main() {
    int x = 5;
    int y = 10;
    if (x == y) {
        return 1;
    } else if (x != y) {
        return 2;
    } else if (x > y) {
        return 3;
    } else if (x < y) {
        return 4;
    } else if (x >= y) {
        return 5;
    } else if (x <= y) {
        return 6;
    }
    return 0;
}
